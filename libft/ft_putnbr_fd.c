#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	aux;

	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	if (n < 0 && n != -2147483648)
	{
		write (fd, "-", 1);
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd (n / 10, fd);
		aux = (n % 10) + '0';
		write (fd, &aux, 1);
	}
	if (n < 10 && n != -2147483648)
	{
		aux = n + '0';
		write (fd, &aux, 1);
	}	
}
